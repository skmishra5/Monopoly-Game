
public class TitleDeedCardRent {
	private int oneHouse;
	private int twoHouse;
	private int threeHouse;
	private int fourHouse;
	private int hotel;
	public TitleDeedCardRent(int oneHouse, int twoHouse, int threeHouse,
			int fourHouse, int hotel) {
		super();
		this.oneHouse = oneHouse;
		this.twoHouse = twoHouse;
		this.threeHouse = threeHouse;
		this.fourHouse = fourHouse;
		this.hotel = hotel;
	}
	public int getOneHouse() {
		return oneHouse;
	}
	public void setOneHouse(int oneHouse) {
		this.oneHouse = oneHouse;
	}
	public int getTwoHouse() {
		return twoHouse;
	}
	public void setTwoHouse(int twoHouse) {
		this.twoHouse = twoHouse;
	}
	public int getThreeHouse() {
		return threeHouse;
	}
	public void setThreeHouse(int threeHouse) {
		this.threeHouse = threeHouse;
	}
	public int getFourHouse() {
		return fourHouse;
	}
	public void setFourHouse(int fourHouse) {
		this.fourHouse = fourHouse;
	}
	public int getHotel() {
		return hotel;
	}
	public void setHotel(int hotel) {
		this.hotel = hotel;
	}

}
